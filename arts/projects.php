        <p class="heading maroonText">Papier-mache Hats</p>
        <p>
            You will need:</p>
        <ul class="crafts">
            <li>
                One large balloon per hat</li>
            <li>
                Old newspaper</li>
            <li>
                Flour glue (see directions)</li>
            <li>
                Scissors</li>
            <li>
                String or elastic</li>
            <li>
                Paint</li>
        </ul>
        <ol>
            <li>
                Make the flour glue by mixing one cup of flour into one cup of water until it is runny. Boil four cups of water and add in the flour-water mixture. Simmer for three minutes. Let cool.</li>
            <li>
                Blow up the balloons.</li>
            <li>
                Help your child tear the newspaper into thin strips.</li>
            <li>
                Dip strips of newspaper into the flour glue and cover the top of the balloon. You will need to make at least layers to have a sturdy hat. You can let the layers dry each time or do them all at once.</li>
            <li>
                Let the hat dry for a few days.</li>
            <li>
                Pop the balloon and trim the edges of the hat. Help your child decorate with paint. You can also glue on construction paper ears to make an animal hat.</li>
            <li>
                Poke small holes in opposite edges of the hat and put the string or elastic through to make a chin strap.</li>
        </ol>
        <p>
            Wear the hats and have a parade or act out a play or story!</p>
        <p class="heading maroonText">Cornstarch and Soda Clay Dough</p>
        <p>
            You will need:</p>
        <ul class="crafts">
            <li>
                2 cups baking soda</li>
            <li>
                1 cup cornstarch</li>
            <li>
                1 1/4 cups cold water</li>
        </ul>
        <ol>
            <li>
                Combine all ingredients in saucepan, stirring until smooth.</li>
            <li>
                Bring to boil and boil one minute, stirring until clay is the consistency of mashed potatoes. Pour out onto a cookie tray and cover with a damp cloth until cool.</li>
            <li>
                Knead lightly, adding food coloring, if desired. Use immediately or store, wrapped airtight, in the refrigerator.</li>
        </ol>
        <p class="heading maroonText">Salt Dough</p>
        <p>
            You will need:</p>
        <ul class="crafts">
            <li>
                1 cup of salt</li>
            <li>
                1 cup of flour</li>
            <li>
                1/2 cup of water (may add more)</li>
        </ul>
        <ol>
            <li>
                In a large bowl, combine the salt and the flour.</li>
            <li>
                Make a well in the salt/flour mixture and add the water.</li>
            <li>
                Knead until smooth and shape into a ball. When not in use, wrap in plastic or store in an airtight container.</li>
        </ol>
        <p>
            HINT: To get softer dough you can add more flour. Add more salt to get more texture. To add color to your dough, add food coloring or paint. Knead to get an even color.</p>
