        <p class="heading maroonText">Paper Bag Kites</p>
        <p>
            You will need:</p>
        <ul class="crafts">
            <li>
                Large brown paper grocery bag</li>
            <li>
                Strong string</li>
            <li>
                Scissors</li>
            <li>
                Hole punch</li>
            <li>
                A number of paper ring reinforcements (the kind you use on paper in binders from any office supply store. You could also substitute masking tape.)</li>
            <li>
                School glue or paste</li>
            <li>
                Paint (any kind)</li>
            <li>
                Crayons, markers, or colored pencils</li>
            <li>
                Paper streamers or crepe paper that you can cut into strips</li>
            <li>
                A few found objects (bits of paper, glitter, buttons — nothing too heavy)</li>
        </ul>
        <ol>
            <li>
                Begin by taking the hole punch and making four holes in the top of the paper bag— one in each of the corners. Add paper ring reinforcements to the holes, or put a small piece of masking tape over the hole and poke through with a pencil. This will ensure that your holes don't tear through.</li>
            <li>
                Next, cut two lengths of string about 30" each.</li>
            <li>
                Tie each end of the strings through a hole in the bag. The goal is to create two loops.</li>
            <li>
                Next, cut another piece of string — again around 30". Loop this new piece of string through the two loops you created and tie in a knot. This piece of string will become the handle of your kite.</li>
            <li>
                You are now ready to decorate the paper bag kite using paint, markers, or whatever else you desire. You can paint designs on the kite or turn the kite into a fish by adding eyes, gills, and fins. You can glue different items to the kite but be sure not to load the kite down with heavy items -- or it will have a hard time staying up in the air.</li>
            <li>
                Use paper streamers as kite tails and glue them to the bottom of the paper bag. You can make your own streamers by cutting crepe paper into strips. Another nifty streamer idea is to take plastic bags and cut them into strips.</li>
            <li>
                Once the glue and paint is dry, the kite can fly. Hold on tightly to the string handle and run so that the wind catches the kite. When the bag fills with air it will float and flutter behind you.</li>
        </ol>
        <p>
            As a variation, you can add a longer handle string to the kite so it will fly higher in the air.</p>
